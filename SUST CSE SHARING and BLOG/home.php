               <h3> History</h3>


<a href="www.sust.edu">Shahjalal University of Science and Technology (SUST)</a> was established in 1986. The university started its academic programme from 14 February 1991 with three departments: physics, chemistry and economics. The campus is located in Kumargaon, approximately five kilometers away from the heart of Sylhet City Centre. The first convocation of SUST was held on 29 April 1998 and the second convocation of the university was held on 6 December 2007.
<br/>
SUST is the first specialized science & technology university of Bangladesh. The medium of instructions is English. It is the most IT enabled university of the country. SUST is the first university in Bangladesh providing whole campus free Wi-fi access for students & staffs.[4] The university introduced an integrated honors course for the first time in Bangladesh[3] and it introduced the semester system (American credit system) from the 1996-97 session. According to the masterplan for the university, within a few years Shahjalal University of Science and Technology will be one of the finest institutions of the region.
<br/>
The university students invented and introduced the paperless 24/7 SMS Based Automated Registration of Admission Test procedure for the first time in Bangladesh. Interested students can complete the registration process through mobile phone messages. Prime Minister Sheikh Hasina launched the system on 13 September 2009. For the invention, the University won an Ambillion Award in a competition of South Asian countries in 2010 and National Award for E-Content and ICT for Development Award 2010.Now, most of the public universities in Bangladesh have adopted this process of registration.

<h3>
Admission
</h3>
SUST enrolls undergraduate, graduate and postgraduate students. Students who want to get admission into SUST need to pass the admission test which is highly competitive. SUST admission test is considered as one of the toughest in Bangladesh. Admission tests are arranged by the specific schools under the authority of admission council. In 2012-2013 session admission test 40,881 candidates competed against of 1,385 seats. The acceptance rate of SUST is 3.39%.
                
